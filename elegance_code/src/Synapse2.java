

public class Synapse2
{
String name,type,preobj,postobj1,postobj2,postobj3,postobj4;
int    sections,flag;
public Synapse2(String name, String type, int sections,String preobj,String postobj1,String postobj2,String postobj3,String postobj4) 
{
	
	flag=0;
	this.name = name;
	this.type = type;
	this.sections = sections;
	this.preobj = preobj;
	this.postobj1 = postobj1;
	this.postobj2 = postobj2;
	this.postobj3 = postobj3;
	this.postobj4 = postobj4;
}

public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public int getSections() {
	return sections;
}
public void setSections(int sections) {
	this.sections = sections;
}

public int getFlag() {
	return flag;
}

public void setFlag(int flag) {
	this.flag = flag;
}	


}